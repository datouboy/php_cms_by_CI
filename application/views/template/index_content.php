        	<h1 class="page-header">攜手國際半導體設備暨材料商 建構國內產業供應鏈</h1>
            <div class="content_inner">
            	<p><span style="font-size: small;">政院毛治國院長及張善政副院長於10月1日下午在經濟部大禮堂，舉辦「國際半導體設備暨材料商來臺投資座談會」，與會業者包括18家國際半導體設備暨材料廠商以及相關協會代表共22人，如國際半導體設備暨材料協會(SEMI)、台灣半導體協會(TSIA )、美商應用材料公司(Applied Materials)、美商科林研發公司(Lam Research)、美商科磊公司(KLA-Tencor)、日商捷時雅邁科公司(JSR)、中德電子材料公司(SunEdison)、美商3M公司等。</span></p>
                <p><span style="font-size: small;">座談會中，由主要國際設備廠商分享在臺投資經驗，並由主管機關回應業者關切之國內投資環境狀況及法規議題。毛院長會中表示希望藉此座談會，鼓勵外商擴大在臺投資製造、研發、服務中心，並促成與國內業者合作，攜手建立國內半導體零組件與材料供應鏈體系。</span></p>
                <p><span style="font-size: small;">由於臺灣連續5年蟬聯全球最大半導體設備市場，國內半導體業者如:台積電、聯電、日月光及矽品等，不斷投入先進製程之開發與導入量產，已形成技術領先之局面，國際半導體設備與材料商紛紛來臺布局，希望搶占國內半導體市場。美商應用材料公司在南科建立製造中心；美商科林研發公司與科磊公司也已分別在臺設立全球訓練中心，均一致看好臺灣半導體市場之前景，對於臺灣成為亞洲半導體設備與材料製造中心願景給予最大支持。毛院長期許國際半導體設備與材料技術領導廠，攜手臺灣半導體及其零組件、材料在地供應鏈業者，三方合作共同發展，持續促進臺灣半導體相關產業繁榮與永續經營。</span></p>
                <p><span style="font-size: small;">經濟部工業局表示，臺灣本土半導體設備與材料供應鏈業者，已經過長期經營與歷練，建構極為完整且效率極高之產業聚落，特別是在精密加工方面已具能量，適合外商納入其供應鏈體系，作為全球設備銷售之合作夥伴。經濟部工業局將全力配合外商需求，媒合適宜之國內業者進行合作，並運用政府資源協助技術開發，共創互利互榮之產業夥伴關係。</span></p>
                <p><span style="font-size: small;">新聞資料來源：<a href="http://www.moea.gov.tw/MNS/populace/news/News.aspx?kind=1&amp;menu_id=40&amp;news_id=46617">中華民國經濟部官方網站</a></span></p>
